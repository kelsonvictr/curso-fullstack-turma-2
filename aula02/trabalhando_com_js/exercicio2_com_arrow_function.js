// Defina a função usando a sintaxe de arrow function
const objetivoDoCurso = (tecnologia) => {
    // Retorne a string concatenada usando template literals
    return `Meu objetivo é aprender ${tecnologia}`;
}

// Defina uma constante tecnologia fora da função
const tecnologia = "React";

// Chame a função e imprima o resultado
console.log(objetivoDoCurso(tecnologia));
